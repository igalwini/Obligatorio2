    public void listaContratosPantalla(Cliente cliente){
        //lstContratos.setListData(modelo.getListaContratos().toArray());
        ArrayList<Contrato>listaContratos = modelo.getListaContratos();
        ArrayList<Contrato>aux=new ArrayList<>();
        Iterator<Contrato>it=listaContratos.iterator();
        while(it.hasNext()){
            Contrato contrato = it.next();
            if(contrato.getCliente()==cliente){
                aux.add(contrato);
            }
        }
        lstContratos.setListData(aux.toArray());
    }


