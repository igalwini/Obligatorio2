    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        try {
            Cliente cliente = (Cliente) lstClientes.getSelectedValue();
            if (lstClientes.getSelectedValue() == null) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar Cliente", "Error", 0);
            } else {
                Empleado empleado = (Empleado) lstEmpleados.getSelectedValue();
                if (lstEmpleados.getSelectedValue() == null) {
                    JOptionPane.showMessageDialog(null, "Debe seleccionar Empleado", "Error", 0);
                } else {
                    Contrato contrato = (Contrato) lstContratos.getSelectedValue();
                    if (lstContratos.getSelectedValue() == null) {
                        JOptionPane.showMessageDialog(null, "Debe seleccionar un Contrato", "Error", 0);
                    } else {

                        if (txtDia.getText().isBlank()) {
                            JOptionPane.showMessageDialog(null, "Debe Ingresar un dia", "Error", 0);
                        } else {
                            int dia = Integer.parseInt(txtDia.getText());
                            if (txtMes.getText().isBlank()) {
                                JOptionPane.showMessageDialog(null, "Debe Ingresar un mes", "Error", 0);
                            } else {
                                int mes = Integer.parseInt(txtMes.getText());
                                
                                if (dia > 30 || dia < 1) {
                                    JOptionPane.showMessageDialog(null, "Dia fuera de rango", "Error", 0);
                                } else {
                                    if(mes>12||mes<1){
                                        JOptionPane.showMessageDialog(null, "Mes fuera de rango", "Error", 0);
                                    }else{
                                        contrato.agregarVisita(dia,mes,empleado);
                                        txtDia.setText("");
                                   txtMes.setText("");
                                    }
                                    
                                }
                            }
                        }

                    }
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos correctamente", "Error", 0);
        }
        

    }//GEN-LAST:event_btnAgregarActionPerformed

